def is_digit(char):
    # Pengecekan apakah karakter adalah digit
    digits = "0123456789"
    return char in digits

def calculator(expression):
    # Inisialisasi variabel untuk operasi dan hasil
    hasil = 0
    operasi = '+'
    angka_sementara = 0

    # Iterasi melalui setiap karakter dalam ekspresi
    for char in expression:
        # Jika karakter adalah angka
        if is_digit(char):
            angka_sementara = angka_sementara * 10 + int(char)
        # Jika karakter adalah operator
        elif char in {'+', '-', '*', '/'}:
            # Hitung hasil sebelumnya berdasarkan operator sebelumnya
            if operasi == '+':
                hasil += angka_sementara
            elif operasi == '-':
                hasil -= angka_sementara
            elif operasi == '*':
                hasil *= angka_sementara
            elif operasi == '/':
                hasil /= angka_sementara

            # Reset angka_sementara dan simpan operator baru
            angka_sementara = 0
            operasi = char

    # Hitung hasil terakhir setelah iterasi selesai
    if operasi == '+':
        hasil += angka_sementara
    elif operasi == '-':
        hasil -= angka_sementara
    elif operasi == '*':
        hasil *= angka_sementara
    elif operasi == '/':
        hasil /= angka_sementara

    return hasil

# Contoh penggunaan
print(calculator("23+4"))     # Output: 27
print(calculator("45-15"))    # Output: 30
print(calculator("13+2-5*2")) # Output: 5
print(calculator("49/7*2-3")) # Output: 11
